-- البحث عن أسماء تبدأ بـ "أ"
SELECT * FROM الطلاب WHERE الاسم LIKE 'أ%';

-- البحث عن أسماء تحتوي "محمد"
SELECT * FROM الطلاب WHERE الاسم LIKE '%محمد%';

-- اختيار من قائمة
SELECT * FROM الطلاب WHERE الاسم IN ('أحمد محمد', 'فاطمة علي', 'علي خالد');

-- عدم الاختيار من قائمة
SELECT * FROM الطلاب WHERE الاسم NOT IN ('أحمد محمد', 'فاطمة علي');